var router = require('express').Router();
var PackageRepository = require('../repository/package-repository');


//======================================================================================================================
// Save package
//======================================================================================================================

router.post('/packages', function(req, res) {
    PackageRepository.save(req.body.package, function (savedPackage) {
        res.send(savedPackage);
    });
});

//======================================================================================================================
// Get package by id
//======================================================================================================================

router.get('/packages', function(req, res) {
    PackageRepository.find(req.query.id, function (foundPackages) {
        res.send(foundPackages);
    });
});

//======================================================================================================================
// Export
//======================================================================================================================

module.exports = router;